CREATE SCHEMA LOUW;
CREATE SEQUENCE LOUW.ACCOUNT_TYPE_SEQ
    START WITH 10000
    INCREMENT BY 1;

CREATE TABLE LOUW.ACCOUNT_TYPE
(
    ACCOUNT_TYPE_ID NUMBER(30) NOT NULL,
    MNEMONIC VARCHAR(30) NOT NULL,
    ACCOUNT_TYPE_NAME VARCHAR(30) NOT NULL,
    CREATION_DATE DATE NOT NULL,
    PRIMARY KEY (ACCOUNT_TYPE_ID)
);

CREATE SEQUENCE LOUW.ACCOUNT_TRANSACTION_SEQ
    START WITH 10000
    INCREMENT BY 1;

CREATE TABLE LOUW.ACCOUNT_TRANSACTION
(
    ACCOUNT_TRANS_ID NUMBER(30) NOT NULL,
    MEMBER_ID NUMBER(30) NOT NULL,
    TRANSACTION_DATE DATE NOT NULL,
    PRIMARY KEY (ACCOUNT_TRANS_ID)
);